import SnapshotTesting
import XCTest
import Reusable
@testable import XCUITest_study

class XCUITest_AssetSnapshotTests: XCTestCase {
  func testViewControllerAssertSnapshot() {
    let vc = ViewController.instantiate()
    
    assertSnapshot(matching: vc, as: .image)
  }
  
  func testTestViewAssertSnapshot() {
    let data = "testScreenshot"
    let view = TestView(frame: CGRect(x: 0, y: 0, width: 200, height: 40))
    view.text = data

    view.layoutIfNeeded()
    let screenshot = view.asImage()
    assertSnapshot(matching: screenshot,
                   as: .image,
                   named: "TestView")
  }
}
